                <h3 style="text-align: justify;">Hidroginástica</h3>
                <p style="text-align: justify;">Com menos intensidade que as outras modalidades, a Hidroginástica trabalha com o corpo parcialmente imerso na água, diminuindo assim o impacto sobre as articulações e coluna vertebral. Apesar de muitas vezes passar despercebida, a Hidroginástica é referência quando se trata de recuperação de lesões e obtenção de resistência muscular. E os benefícios não param por aí: quem pratica esta atividade, ganha maior flexibilidade e melhora a postura. <br><br> A Ciafit oferece as melhores condições para atividades aquáticas: a piscina é aquecida em temperatura agradável para os alunos. Na Hidroginástica, a equipe de instrutores da academia monta o programa de aulas de acordo com um cronograma estabelecido, que é atualizado todos os meses. Nele estão as atividades que serão trabalhadas em cada encontro. Acompanhamento periódico e contínuo para o aluno manter a disciplina e o prazer de se exercitar.</p>
                <h3 style="text-align: justify;">Natação</h3>
                <p style="text-align: justify;">A mais completa das modalidades, atuante  sobre o maior número de músculos e órgãos. A natação melhora a capacidade respiratória e mantém o ritmo da circulação sanguínea do praticante. Ainda por cima, diminui a intensidade dos impactos sobre as articulações do aluno.<br><br> Ideal para todas as idades e tipos físicos, a natação na Ciafit é monitorada pelos mais competentes profissionais da área, e praticada em piscina térmica, aliando o exercício ao conforto e relaxamento do aluno. Dos bebês ao grupo da terceira idade, todas as faixas etárias encontram seu espaço nas turmas de natação da academia.</p>
